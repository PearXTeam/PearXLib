namespace PearXLib
{
	/// <summary>
	/// I18n language.
	/// </summary>
	public class I18nLang
	{
		/// <summary>
		/// Language name. For example, en_US.
		/// </summary>
		public string Name { get; set; }

		/// <summary>
		/// User readable language name. For example, "Russian".
		/// </summary>
		public string LangName { get; set; }
	}
}
